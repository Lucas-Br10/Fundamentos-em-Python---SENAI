# Conversor de Temperatura 

"""
solicite ao usuario para fornecer uma temperatura em graus e converta essa temperatura em farehrenheit
"""

"""
multiplicar a temperatura em graus Celsius por 1,8 e somar 32.
"""

print("coloque a temperatura em graus celcius")
temperatura = float(input())


print("convertendo para farenheit")
farenheit = (temperatura * 1.8) + 32



print("resultado", farenheit) 


